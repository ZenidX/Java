
public class Main {
	public static void EjercicioOperadoresCombinados() {
		int a=25;
		System.out.println("a+=5  --> "+(a+=5));a=25;
		System.out.println("a-=15 --> "+(a-=15));a=25;
		System.out.println("a*=2 --> "+(a*=2));a=25;
		System.out.println("a/=2 --> "+(a/=2));a=25;
		System.out.println("a%=3 --> "+(a%=3));
	}
	public static void main(String[] args) {
		EjercicioOperadoresCombinados();
	}
}
